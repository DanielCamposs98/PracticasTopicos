package ClasesBase;


public class Dias {
    
    private int claveEmpleado, dias;

    public Dias(int claveEmpleado, int dias) {
        this.claveEmpleado = claveEmpleado;
        this.dias = dias;
    }

    public int getClaveEmpleado() {
        return claveEmpleado;
    }

    public int getDias() {
        return dias;
    }
    
    
}
